//Convert Each element in array to Uppercase
var arr1 = ['apple','banana','grapes','mangoes'];
var arr1ToUpper = arr1.map(ele => ele.toUpperCase());
console.log(arr1ToUpper);

//Square of each element in an array
var arr2 = [1,2,3,4,5];
var var2Square = arr2.map(ele => ele*ele);
console.log(var2Square);
//Bitwise AND
let result1 = 5 & 3; // Binary: 0101 & 0011 = 0001 (Decimal: 1)
console.log(result1); // 1

//Bitwise OR
let result2 = 5 | 3; // Binary: 0101 | 0011 = 0111 (Decimal: 7)
console.log(result2); // 7

//Bitwise XOR
let result3 = 5 ^ 3; // Binary: 0101 ^ 0011 = 0110 (Decimal: 6)
console.log(result3); // 6

//Bitwise NOT
let result4 = ~5; // Binary: ~0101 = 1010 (Decimal: -6)
console.log(result4); // -6

//Left Shift
let result5 = 5 << 1; // Binary: 0101 << 1 = 1010 (Decimal: 10)
console.log(result5); // 10

//Right Shift
let result6 = 5 >> 1; // Binary: 0101 >> 1 = 0010 (Decimal: 2)
console.log(result6); // 2

//Zero-fill Right Shift
let result7 = -5 >>> 1; // Binary: -0101 >>> 1 = 0111 (Decimal: 7)
console.log(result7); // 7






